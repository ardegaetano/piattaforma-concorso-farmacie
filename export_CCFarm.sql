  CREATE OR REPLACE PROCEDURE "CC_FARM"."P_CONTROLLO_GRAD" ( p_COD_REG VARCHAR2  )
IS

  cursor cur_Cand is
  SELECT distinct id_candidatura id_cand 
  FROM candidatura_reg 
  where cod_reg_utente = p_COD_REG
  and ammesso = 'SI'
--  and id_candidatura = '27085'
  ;
   
  tot_ALTRA_LAUREA_BIS_REG            number(8,4);
  tot_ALTRA_LAUREA_REG                number(8,4);
  tot_ALTRO_TITOLO_REG                number(8,4);
  tot_ALTRO_TITOLO_REG_C              number(8,4);
  tot_ALTRO_TITOLO_REG_H              number(8,4);
  tot_BORSA_STUDIO_REG                number(8,4);     
  tot_CORSO_AGGIORNAMENTO_REG         number(8,4);
  tot_DOTTORATO_REG                   number(8,4);
  tot_IDONEITA_REG                    number(8,4);
  tot_IDONEITA_REG_NAZ                number(8,4);
  tot_PUBBLICAZIONE_REG               number(8,4);
  tot_SPECIALIZZAZIONE_REG            number(8,4);
  tot_REQ_MINIMI_REG_LAUREA           number(8,4);
  tot_REQ_MINIMI_REG_ABB              number(8,4);
  flag_titolo_altro                              varchar2(5);
  flag_titolo_dott                             varchar2(5);
  AGGR_A       number(8,4);
  AGGR_B       number(8,4);

  tot_titoli   number(8,4);
  tot_ESP_GRAD number(8,4);
  tot_GRAD     number(8,4);

BEGIN

   -- per ogni candidatura controllo il totale dei punteggi dei titoli + esperienze e controllo che il valore sia lo stesso della graduatoria
   FOR rec_cand in cur_Cand  
   LOOP

            tot_ALTRA_LAUREA_BIS_REG      := 0;
            tot_ALTRA_LAUREA_REG          := 0;
            tot_ALTRO_TITOLO_REG          := 0;
            tot_ALTRO_TITOLO_REG_C          := 0;
            tot_ALTRO_TITOLO_REG_H          := 0;
            tot_BORSA_STUDIO_REG          := 0;     
            tot_CORSO_AGGIORNAMENTO_REG   := 0;
            tot_DOTTORATO_REG             := 0;
            tot_IDONEITA_REG              := 0;
            tot_PUBBLICAZIONE_REG         := 0;
            tot_SPECIALIZZAZIONE_REG      := 0;
            tot_REQ_MINIMI_REG_LAUREA     := 0;
            tot_REQ_MINIMI_REG_ABB        := 0;
            
            AGGR_A       := 0;
            AGGR_B       := 0;
            
            tot_titoli                    := 0;
            tot_ESP_GRAD                  := 0;
            tot_GRAD                      := 0;


             -- SOMMA TITOLI 
             select  NVL(sum(PUNTEGGIO),0 )
             into tot_ALTRA_LAUREA_BIS_REG
             from ALTRA_LAUREA_BIS_REG 
             where id_domanda_laurea_bis in ( select a.ID_UTENTE from UTENTE_REG a , candidatura_reg b
                                              where a.COD_REG_UTENTE = p_COD_REG  
                                                and a.id_UTENTE = b.id_UTENTE
                                                and b.id_candidatura = rec_cand.id_cand );
                                                
              if   tot_ALTRA_LAUREA_BIS_REG  > to_number('3,5') then tot_ALTRA_LAUREA_BIS_REG :=  to_number('3,5'); end if;                                             
                                                              

              select NVL(  sum( PUNTEGGIO ) ,0 )
              into tot_ALTRA_LAUREA_REG
              from ALTRA_LAUREA_REG 
              where  id_domanda_laurea in  ( select a.id_UTENTE from UTENTE_REG a , candidatura_reg b
                                              where a.COD_REG_UTENTE = p_COD_REG  
                                                and a.id_UTENTE = b.id_UTENTE
                                                and b.id_candidatura = rec_cand.id_cand );
              
              if   tot_ALTRA_LAUREA_REG  > to_number('1,5') then tot_ALTRA_LAUREA_REG :=  to_number('1,5'); end if;                                                                               
                                                

              select NVL(  sum( PUNTEGGIO ) ,0 )
              into tot_ALTRO_TITOLO_REG
              from ALTRO_TITOLO_REG 
              where  id_domanda_titolo in ( select a.id_UTENTE from UTENTE_REG a , candidatura_reg b
                                              where a.COD_REG_UTENTE = p_COD_REG  
                                                and a.id_UTENTE = b.id_UTENTE
                                                and b.id_candidatura = rec_cand.id_cand ) and flag_titolo is null ;
            
           select NVL(  sum( PUNTEGGIO ) ,0 )
              into tot_ALTRO_TITOLO_REG_C
              from ALTRO_TITOLO_REG 
              where  id_domanda_titolo in ( select a.id_UTENTE from UTENTE_REG a , candidatura_reg b
                                              where a.COD_REG_UTENTE = p_COD_REG  
                                                and a.id_UTENTE = b.id_UTENTE
                                                and b.id_candidatura = rec_cand.id_cand ) and flag_titolo='C' ;
           
           select NVL(  sum( PUNTEGGIO ) ,0 )
              into tot_ALTRO_TITOLO_REG_H
              from ALTRO_TITOLO_REG 
              where  id_domanda_titolo in ( select a.id_UTENTE from UTENTE_REG a , candidatura_reg b
                                              where a.COD_REG_UTENTE = p_COD_REG  
                                                and a.id_UTENTE = b.id_UTENTE
                                                and b.id_candidatura = rec_cand.id_cand ) AND flag_titolo='H';                                    

              select NVL(  sum( PUNTEGGIO ) ,0 ) 
              into tot_BORSA_STUDIO_REG
              from BORSA_STUDIO_REG 
              where  id_domanda_borsa in ( select a.id_UTENTE from UTENTE_REG a , candidatura_reg b
                                              where a.COD_REG_UTENTE = p_COD_REG  
                                                and a.id_UTENTE = b.id_UTENTE
                                                and b.id_candidatura = rec_cand.id_cand );
                                                

              select NVL(  sum( PUNTEGGIO ) ,0 )
              into tot_CORSO_AGGIORNAMENTO_REG
              from CORSO_AGGIORNAMENTO_REG 
              where  id_domanda_agg in  ( select a.id_UTENTE from UTENTE_REG a , candidatura_reg b
                                              where a.COD_REG_UTENTE = p_COD_REG  
                                                and a.id_UTENTE = b.id_UTENTE
                                                and b.id_candidatura = rec_cand.id_cand );
                                                

              select NVL(  sum( PUNTEGGIO ) ,0 )
              into tot_DOTTORATO_REG
              from DOTTORATO_REG 
              where  id_domanda_dottorato in ( select a.id_UTENTE from UTENTE_REG a , candidatura_reg b
                                              where a.COD_REG_UTENTE = p_COD_REG  
                                                and a.id_UTENTE = b.id_UTENTE
                                                and b.id_candidatura = rec_cand.id_cand );


              select NVL(sum( PUNTEGGIO) ,0 )
              into tot_IDONEITA_REG
              from IDONEITA_REG idone
              where  id_domanda_idon in ( select a.id_UTENTE from UTENTE_REG a , candidatura_reg b
                                              where a.COD_REG_UTENTE = p_COD_REG  
                                                and a.id_UTENTE = b.id_UTENTE
                                                and b.id_candidatura = rec_cand.id_cand );
              
              if   tot_IDONEITA_REG  > to_number('1') then tot_IDONEITA_REG :=  to_number('1'); end if;                                                                               


              select NVL( sum(PUNTEGGIO_NAZ),0 )
              into tot_IDONEITA_REG_NAZ
              from IDONEITA_REG idon
              where  idon.id_domanda_idon in ( select a.id_UTENTE from UTENTE_REG a , candidatura_reg b
                                              where a.COD_REG_UTENTE = p_COD_REG  
                                                and a.id_UTENTE = b.id_UTENTE
                                                and b.id_candidatura = rec_cand.id_cand ) ;
                                                                                                
              if   tot_IDONEITA_REG_NAZ  > to_number('1') then tot_IDONEITA_REG_NAZ :=  to_number('1'); end if;                                                                               
                                                

              select NVL(  sum( PUNTEGGIO ) ,0 )
              into tot_PUBBLICAZIONE_REG
              from PUBBLICAZIONE_REG 
              where  id_domanda_pubbl in  ( select a.id_UTENTE from UTENTE_REG a , candidatura_reg b
                                              where a.COD_REG_UTENTE = p_COD_REG  
                                                and a.id_UTENTE = b.id_UTENTE
                                                and b.id_candidatura = rec_cand.id_cand );

              if   tot_PUBBLICAZIONE_REG  > to_number('1') then tot_PUBBLICAZIONE_REG :=  to_number('1'); end if;                                                                               


              select NVL(  sum( PUNTEGGIO ) ,0 )
              into tot_SPECIALIZZAZIONE_REG
              from SPECIALIZZAZIONE_REG 
              where  id_domanda_spec in  ( select a.id_UTENTE from UTENTE_REG a , candidatura_reg b
                                              where a.COD_REG_UTENTE = p_COD_REG  
                                                and a.id_UTENTE = b.id_UTENTE
                                                and b.id_candidatura = rec_cand.id_cand );
                                                
                                                

              select nvl( sum( punteggio_laurea ) ,0) 
              into  tot_REQ_MINIMI_REG_LAUREA
              from   REQUISITI_MINIMI_REG  
              where  id_domanda_req  in ( select a.id_UTENTE from UTENTE_REG a , candidatura_reg b
                                              where a.COD_REG_UTENTE = p_COD_REG  
                                                and a.id_UTENTE = b.id_UTENTE
                                                and b.id_candidatura = rec_cand.id_cand );
                                                
              if   tot_REQ_MINIMI_REG_LAUREA  > to_number('5') then tot_REQ_MINIMI_REG_LAUREA :=  to_number('5'); end if;                                                                               

                                                
              select  NVL(  sum(  punteggio_abilitazione  )  ,0 )
              into  tot_REQ_MINIMI_REG_ABB
              from   REQUISITI_MINIMI_REG  
              where  id_domanda_req  in ( select a.id_UTENTE from UTENTE_REG a , candidatura_reg b
                                              where a.COD_REG_UTENTE = p_COD_REG  
                                                and a.id_UTENTE = b.id_UTENTE
                                                and b.id_candidatura = rec_cand.id_cand );
                                                

                                    
             --   SELECT FLAG_TITOLO
              --  INTO flag_titolo_altro
               -- FROM ALTRO_TITOLO_REG
                --WHERE ID_DOMANDA_TITOLO IN(select a.ID_UTENTE from UTENTE a , candidatura_reg b
                 --                             where a.COD_REG_UTENTE = p_COD_REG  
                  --                              and a.id_utente = b.id_utente
                   --                             and b.id_candidatura = rec_cand.id_cand);
                                                
             --     SELECT FLAG_TITOLO
             --   INTO flag_titolo_dott
              --  FROM dottorato_REG
               -- WHERE ID_DOMANDA_DOTTORATO IN(select a.ID_UTENTE from UTENTE a , candidatura_reg b
                --                              where a.COD_REG_UTENTE = p_COD_REG  
                 --                               and a.id_utente = b.id_utente
                  --                              and b.id_candidatura = rec_cand.id_cand);

              -- CONTROLLO TAGLI AGGREGATI 

           -- IF FLAG_TITOLO_altro IS NOT NULL THEN
           -- IF FLAG_TITOLO_altro = 'H' THEN 
            --AGGR_A := ( tot_REQ_MINIMI_REG_ABB + tot_CORSO_AGGIORNAMENTO_REG + tot_ALTRO_TITOLO_REG) ;
            --else
            --AGGR_B := (tot_SPECIALIZZAZIONE_REG + tot_BORSA_STUDIO_REG + tot_ALTRO_TITOLO_REG ) ;
            -- end if;
            -- END IF;
             
           --  if flag_titolo_dott IS NOT NULL THEN
            -- if flag_titolo_dott ='H' then
            -- AGGR_A := ( tot_REQ_MINIMI_REG_ABB + tot_CORSO_AGGIORNAMENTO_REG +  tot_DOTTORATO_REG ) ;
            -- else
            -- AGGR_B := (tot_SPECIALIZZAZIONE_REG + tot_BORSA_STUDIO_REG + tot_DOTTORATO_REG ) ;
            -- end if;
             --END IF;

          AGGR_A := nvl(  ( tot_REQ_MINIMI_REG_ABB + tot_CORSO_AGGIORNAMENTO_REG +tot_ALTRO_TITOLO_REG_H) ,0 );
           
          AGGR_B := nvl( (tot_SPECIALIZZAZIONE_REG + tot_BORSA_STUDIO_REG + tot_DOTTORATO_REG +tot_ALTRO_TITOLO_REG_C),0)  ;

            
            if AGGR_A > to_number('0,5') then  AGGR_A := to_number('0,5'); end if;
            if AGGR_B > to_number('2')   then  AGGR_B := to_number('2');  end if;


             tot_titoli := nvl(  ( tot_ALTRA_LAUREA_BIS_REG + tot_ALTRA_LAUREA_REG  +      
                             tot_IDONEITA_REG  +  tot_IDONEITA_REG_NAZ + tot_PUBBLICAZIONE_REG   +
                             tot_REQ_MINIMI_REG_LAUREA + AGGR_A + AGGR_B ) , 0 ) ;
              

              if tot_titoli > 15 then tot_titoli := 15; end if;
              
              
              -- SOMMA GRADUATORIA
              select nvl( sum( punteggio_esperienza ), 0)   , nvl(  sum( PUNTEGGIO )  ,0 ) 
              into tot_ESP_GRAD, tot_GRAD
              from GRADUATORIA 
              where cod_regionale = p_COD_REG
              and id_candidatura  = rec_cand.id_cand;


              -- CONTROLLO CONGRUENZA 
              if ( tot_titoli + tot_ESP_GRAD ) != tot_GRAD then
              
                  insert into GRD_Errate
                  ( codice_reg ,id_cand, punt_grad, punt_titolo_cand, punt_esp_prof_cand, punt_cand_tot ,
                    tot_ALTRA_LAUREA_BIS_REG , tot_ALTRA_LAUREA_REG, tot_IDONEITA_REG, tot_PUBBLICAZIONE_REG,
                    tot_REQ_MINIMI_REG_LAUREA , AGGR_A , AGGR_B, tot_IDONEITA_REG_NAZ )
                  values
                  (  p_COD_REG , rec_cand.id_cand, tot_GRAD, tot_titoli, tot_ESP_GRAD, tot_titoli + tot_ESP_GRAD ,
                     tot_ALTRA_LAUREA_BIS_REG , tot_ALTRA_LAUREA_REG, tot_IDONEITA_REG, tot_PUBBLICAZIONE_REG,
                     tot_REQ_MINIMI_REG_LAUREA , AGGR_A , AGGR_B, tot_IDONEITA_REG_NAZ
                   );
                  
                  commit;
              
              end if;

   END LOOP;
   
   

   EXCEPTION
     WHEN OTHERS THEN
     
     insert into GRD_Errate
                  ( codice_reg ,id_cand, punt_grad, punt_titolo_cand, punt_esp_prof_cand, punt_cand_tot  )
                  values
                  (  p_COD_REG , 0, 0, 0, 0, 0 );
                  
                  commit;

END p_CONTROLLO_GRAD;

