      r_ccc                VARCHAR2 (3)  := NULL;
      r_nnn                VARCHAR2 (3)  := NULL;
      r_aa                 VARCHAR2 (2)  := NULL;
      r_aacf72             VARCHAR2 (2)  := NULL;
      r_m                  VARCHAR2 (1)  := NULL;
      r_gg                 VARCHAR2 (2)  := NULL;
      r_lvvv               VARCHAR2 (4)  := NULL;
      r_vvv                VARCHAR2 (3)  := NULL;
      r_k                  VARCHAR2 (1)  := NULL;
      r_cf15               VARCHAR2 (20) := NULL;
      r_ggcf102            VARCHAR2 (20) := NULL;
      r_vvvcf133           VARCHAR2 (20) := NULL;
      decadederivata       VARCHAR2 (1);
      annoderivato         VARCHAR2 (1);
      decinaderivata       VARCHAR2 (1);
      giornoderivato       VARCHAR2 (1);
      cifraluogo1          VARCHAR2 (20);
      cifraluogo2          VARCHAR2 (20);
      cifraluogo3          VARCHAR2 (20);
  
      resto                NUMBER;
      processo             VARCHAR2 (1);
