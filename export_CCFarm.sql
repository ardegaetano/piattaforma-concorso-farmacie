  CREATE OR REPLACE FUNCTION "CC_FARM"."F_COD_FISC" (
      w_cognome               IN   VARCHAR2 := NULL,
      w_nome                  IN   VARCHAR2 := NULL,
      w_data_nascita          IN   DATE := NULL,
      w_sesso                 IN   VARCHAR2 := NULL, 
