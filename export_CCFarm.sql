---------------------------------------------------
-- PREPARAZIONE CAMPI MAIUSCOLI E PULITI: COGNOME
      p_cognome :=  replace (UPPER (TRANSLATE (LOWER (w_cognome), 'àaeeèéiou', 'AAEEEEIOU')), '-', ' ');
       
      
      r_ccc :=
         SUBSTR (   SUBSTR (REPLACE (TRANSLATE (p_cognome, 'AEIOU ''', ' '),
                                     ' '
                                    ),
                            1,
                            3
                           )
                 || SUBSTR (REPLACE (TRANSLATE (p_cognome,
                                                'BCDFGHJKLMNPQRSTVWXYZ ''',
                                                ' '
                                               ),
                                     ' '
                                    ),
                            1,
                            3
                           )
                 || 'XXX',
                 1,
                 3
                );
------------------------------------------------
-- NOME: 3 CARATTERI (POSIIONE DEL CF: 4,5,6) --
------------------------------------------------
-- PREPARAZIONE CAMPI MAIUSCOLI E PULITI: NOME
      p_nome := replace ( UPPER (TRANSLATE (LOWER (w_nome), 'àaeeèéiou', 'AAEEEEIOU')), '-', ' ');

      IF (LENGTH (REPLACE (TRANSLATE (p_nome, 'AEIOU ''', ' '), ' ')) > 3)
      THEN
         r_nnn :=
               SUBSTR (REPLACE (TRANSLATE (p_nome, 'AEIOU ''', ' '), ' '),
                       1,
                       1
                      )
            || SUBSTR (REPLACE (TRANSLATE (p_nome, 'AEIOU ''', ' '), ' '),
                       3,
                       2
                      );
      ELSE
         r_nnn :=
            SUBSTR
                (   SUBSTR (REPLACE (TRANSLATE (p_nome, 'AEIOU ''', ' '), ' '),
                            1,
                            3
                           )
                 || SUBSTR (REPLACE (TRANSLATE (p_nome,
                                                'BCDFGHJKLMNPQRSTVWXYZ ''',
                                                ' '
                                               ),
                                     ' '
                                    ),
                            1,
                            3
                           )
                 || 'XXX',
                 1,
                 3
                );
      END IF;

---------------------------------------------------------
-- ANNO DI NASCITA: 2 CARATTERI (POSIIONE DEL CF: 7,8) --
---------------------------------------------------------
      r_aa := TO_CHAR (w_data_nascita, 'YY');

--------------------------------------------------------
-- MESE DI NASCITA: 1 CARATTERE  (POSIIONE DEL CF: 9) --
-------------------------------------------------------
      SELECT DECODE (TO_CHAR (w_data_nascita, 'MM'),
                     '01', 'A',
                     '02', 'B',
                     '03', 'C',
                     '04', 'D',
                     '05', 'E',
                     '06', 'H',
                     '07', 'L',
                     '08', 'M',
                     '09', 'P',
                     '10', 'R',
                     '11', 'S',
                     '12', 'T'
                    )
        INTO r_m
        FROM DUAL;

--------------------------------------------------------
-- GIORNO DI NASCITA E SESSO (POSIIONE DEL CF: 10,11) --
--------------------------------------------------------
      SELECT SUBSTR (TO_CHAR (  TO_CHAR (w_data_nascita, 'DD')
                              + DECODE (UPPER (w_sesso), 'F', 40, 0),
                              '09'
                             ),
                     2
                    )
        INTO r_gg
