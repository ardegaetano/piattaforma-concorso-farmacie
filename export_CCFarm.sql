                 utente u,
                 candidatura c,
                 valori_codice v1,
                 valori_codice v2,
                 valori_codice v3
           WHERE     r.cod_reg = u.COD_REG_UTENTE
                 AND u.id_utente = c.id_utente
                 AND NVL (C.REFERENTE_DOMANDA, 'Y') = 'Y'
                 AND c.stato_registrazione = v1.codice
